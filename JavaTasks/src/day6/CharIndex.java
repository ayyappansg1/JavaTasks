package day6;

public class CharIndex {

	public static void main(String[] args) {
		String name="GreensTechnology";
		System.out.println(name.lastIndexOf("o"));
		
		String name2="SeleniumAutomationTool";
		System.out.println(name2.indexOf("o"));
		
		String name3="Velumurugan";
		System.out.println(name3.indexOf("n"));
		
		String name4="j a v a p r o g r a m";
		System.out.println(name4.lastIndexOf(" "));
		
		String name5="9095484678";
		System.out.println(name5.lastIndexOf("8"));
			
		
		
	}
}
